
	
	
//	1. Multiplication Table

#include <iostream>
using namespace std;

int main(){

          int a=1;
          while(a<=20){
                      
                    int b=1;
                    while(b<=10){
                    	
                    	cout << a << " x " << b << " = " << a * b << endl;
                    	b++;
					}
					cout<<endl;
					a++;
		  }



	return 0;
}





